    private void fireUpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fireUpdateBtnActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tblModel = (DefaultTableModel)fireStationJTable.getModel();
        
        
        if (fireStationJTable.getSelectedRowCount() == 1){
            
            Integer stationId = Integer.valueOf(tblModel.getValueAt(fireStationJTable.getSelectedRow(), 0).toString()); 
            //String hospitalName = stationNameTextField.getText(); 
            Integer loccationId = Integer.valueOf(jLocationId.getText()); 
            String email = jEmailId.getText();
            //Integer contactNum = Integer.valueOf(jContactNum.getText()); 
            
           
            
            tblModel.setValueAt(loccationId, fireStationJTable.getSelectedRow(), 2);
            tblModel.setValueAt(email, fireStationJTable.getSelectedRow(), 3);
            
            
            Map<String, String> mp = new HashMap<>();
            //mp.put("name", String.valueOf(hospitalName));
            mp.put("locationid", String.valueOf(loccationId));
            mp.put("email", String.valueOf(email));
            //mp.put("contactno", String.valueOf(contactNum));
            
            try {
                Helper.updateColumns("firestation", mp, "station_id = " + stationId);
            } 
            
            catch (SQLException ex) {
                System.out.println("Failed");
                Logger.getLogger(CasePortal.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            tblModel.setRowCount(0);
            
            try {
          
            List<FireStation> objList = Helper.getResultSet(FireStation.class, "firestation");
            
            for(FireStation i : objList){
                Integer iD = i.getStationId();
                //String name = i.getName();
                Integer locationId = i.getLocationId();
                String emailId = i.getEmail(); 
                //Integer contactNo = i.getContactNum(); 
                Object[] obj = {iD,locationId, emailId,};
                tblModel.addRow(obj);                   
            }
            
        } catch (InstantiationException ex) {
            Logger.getLogger(CasePortal.class.getName()).log(Level.SEVERE, null, ex);
        }
           
        }
        
    }//GEN-LAST:event_fireUpdateBtnActionPerformed

